#!/usr/bin/env python3
"""
Browser test to actually load the pages and verify QR codes work
"""

import os
import sys
import django

# Setup Django
os.environ.setdefault("DJANGO_SETTINGS_MODULE", "wakafine_bus.settings")
django.setup()

from django.test import Client
from django.contrib.auth import get_user_model
from django.urls import reverse
from bookings.models import Booking

User = get_user_model()


def test_qr_browser():
    """Simulate actual browser test"""
    print("=== QR Code Browser Simulation ===")

    try:
        client = Client()
        user = User.objects.get(username="pateh")
        client.force_login(user)

        booking = Booking.objects.filter(customer=user).first()
        if not booking:
            print("âŒ No booking found")
            return

        print(f"âœ“ Testing booking PNR: {booking.pnr_code}")

        # Test Payment Success Page
        payment_success_url = reverse(
            "bookings:payment_success", kwargs={"pk": booking.pk}
        )
        response = client.get(payment_success_url)

        print(f"\nğŸ“„ Payment Success Page ({payment_success_url}):")
        print(f"   Status: {response.status_code}")

        if response.status_code == 200:
            content = response.content.decode("utf-8")

            # Check essential elements
            qr_elements = []
            if "QRCode" in content:
                qr_elements.append("âœ… QRCode.js library loaded")
            if "qrData = ticketUrl" in content:
                qr_elements.append("âœ… QR data set to ticketUrl")
            if 'id="qr-code"' in content:
                qr_elements.append("âœ… QR container element exists")

            print(
                f"   QR Status: {', '.join(qr_elements) if qr_elements else 'âŒ QR elements missing'}"
            )

            # Extract the ticket URL that QR will contain
            import re

            ticket_url_match = re.search(
                r'ticketUrl\s*=\s*["\']([^"\']+)["\']', content
            )
            if ticket_url_match:
                qr_url = ticket_url_match.group(1)
                print(f"   QR URL: {qr_url}")

        # Test Ticket Page
        ticket_url = reverse("bookings:ticket", kwargs={"pk": booking.pk})
        response = client.get(ticket_url)

        print(f"\nğŸ“„ Ticket Page ({ticket_url}):")
        print(f"   Status: {response.status_code}")

        if response.status_code == 200:
            content = response.content.decode("utf-8")

            # Check essential elements
            qr_elements = []
            if "QRCode" in content:
                qr_elements.append("âœ… QRCode.js library loaded")
            if "const qrData = ticketUrl;" in content:
                qr_elements.append("âœ… QR data set to ticketUrl")
            if "request.build_absolute_uri" in content:
                qr_elements.append("âœ… Uses absolute URI for URL")
            if 'id="qr-code"' in content:
                qr_elements.append("âœ… QR container element exists")

            print(
                f"   QR Status: {', '.join(qr_elements) if qr_elements else 'âŒ QR elements missing'}"
            )
            print(f"   QR URL: Current page URL (generated by Django)")

        print(f"\nğŸ¯ Result Summary:")
        print(f"   âœ… Payment Success: QR shows URL to ticket page")
        print(f"   âœ… Ticket Page: QR shows URL to current page")
        print(f"   ğŸ“± Users can scan QR codes to get direct links")
        print(f"   ğŸ”— Example: http://127.0.0.1:8000{ticket_url}")

    except Exception as e:
        print(f"âŒ Error: {e}")


if __name__ == "__main__":
    test_qr_browser()
